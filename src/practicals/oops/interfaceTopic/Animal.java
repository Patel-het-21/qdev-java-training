package practicals.oops.interfaceTopic;

/**
 * Represents the Animal interface. Any class implementing this interface must
 * provide an implementation of @code makeSound().
 * 
 * @author Het Patel
 * @since 13/10/25
 */
public interface Animal {

	/**
	 * Abstract method to define the sound an animal makes. Must be implemented by
	 * all subclasses.
	 */
	void makeSound();

}